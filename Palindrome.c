#include<stdio.h>
#include<string.h>
int main(){
    char str[10];
    printf("Gimme a string:\n");
    gets(str);
    char rev[10];
    strcpy( rev, str);
    //puts(str);
    strrev(rev);
    //puts(rev);
    int result = strcmp(str,rev);
    if (result == 0){
        printf("It is a palindrome");
    }
    else{
        printf("NOT a palindrome");
    }
}
